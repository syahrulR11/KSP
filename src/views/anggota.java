    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        // TODO add your handling code here:
        try {
            String pattern = "yyyy-MM-dd";
            SimpleDateFormat simpleDateFormat = new SimpleDateFormat(pattern);
            String date = simpleDateFormat.format(txttanggal.getDate());
            UUID uuid = UUID.randomUUID();
            String[] insertColumns = {"id", "nama", "email", "no_telp", "alamat", "tgl_daftar"};
            Object[] insertValues = {uuid.toString(), txtnm.getText(), txtemail.getText(), txttlp.getText(), txtalamat.getText(), date};
            dmlSql.insertData("pegawai", insertColumns, insertValues);
            dbManager.close();
            JOptionPane.showMessageDialog(null, "data berhasil disimpan!");
            kosong();
            txtnm.requestFocus();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "data gagal disimpan!"+e);
        }
        datatable();
    }//GEN-LAST:event_bsimpanActionPerformed

