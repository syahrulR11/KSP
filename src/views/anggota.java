    protected void datatable(){
        Object[] Baris ={"Nama","Email","No. Telp","Jenis Kelamin","Alamat","Jabatan"};
        tabmode = new DefaultTableModel(null,Baris);
        String cariitem=txtcari.getText();
        try{
            String[] columnsToSelect = {"pegawai.nama","pegawai.email","pegawai.no_telp","pegawai.jenis_kelamin","pegawai.alamat","jabatan.nama"};
            String[] joinTable = {"jabatan"};
            String[] joinCondition = {"pegawai.jabatan_id = jabatan.id"};
            String[] conditionColumns = {"pegawai.nama"};
            String[] operators = {"LIKE"};
            Object[] conditionValues = {"%"+cariitem+"%"};
            List<Object[]> results = dmlSql.selectData("pegawai", columnsToSelect, joinTable, joinCondition, conditionColumns, operators, conditionValues, true);
            for (Object[] row : results) {
                tabmode.addRow(row);
            }
            tblanggota.setModel(tabmode);
            dbManager.close();
        } catch (SQLException e){
            JOptionPane.showMessageDialog(null, "data gagal di panggil"+e);
        }
    }

